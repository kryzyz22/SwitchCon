void send_buttons() {
  xSemaphoreTake(xSemaphore, portMAX_DELAY);
  
  report30[0] = timer;
  dummy[0] = timer;

  but1_send = 0;
  but2_send = 0;
  but3_send = 0;

  // --- LECTURA DE LA PALANCA (D-PAD) ---
  bool dpad_u = (gpio_get_level(25) == 0); 
  bool dpad_l = (gpio_get_level(26) == 0); 
  bool dpad_d = (gpio_get_level(27) == 0); 
  bool dpad_r = (gpio_get_level(22) == 0); 

  // --- LECTURA DE BOTONES PRINCIPALES ---
  bool btn_a  = (gpio_get_level(14) == 0); 
  bool btn_b  = (gpio_get_level(12) == 0); 
  bool btn_x  = (gpio_get_level(19) == 0); 
  bool btn_y  = (gpio_get_level(17) == 0); 
  bool btn_l  = (gpio_get_level(18) == 0); 
  bool btn_r  = (gpio_get_level(23) == 0); 
  bool btn_zl = (gpio_get_level(5)  == 0); 
  bool btn_zr = (gpio_get_level(16) == 0); 

  // --- MENÚS Y LATERALES (SL / SR) ---
  bool btn_select = (gpio_get_level(15) == 0); 
  bool btn_sl     = (gpio_get_level(13) == 0); 
  bool btn_sr     = (gpio_get_level(21) == 0); 

  // --- MAPEO DE BITS EN MODO JOYCON_L HORIZONTAL ---
  but3_send = (dpad_d << 0) +   // Down
              (dpad_u << 1) +   // Up
              (dpad_r << 2) +   // Right
              (dpad_l << 3) +   // Left
              (btn_sr << 4) +   // SR lateral (Start)
              (btn_sl << 5) +   // SL lateral (Select físico)
              (btn_l  << 6) +   // L
              (btn_zl << 7);    // ZL

  but1_send = (btn_y  << 0) +   // Y
              (btn_x  << 1) +   // X
              (btn_b  << 2) +   // B
              (btn_a  << 3) +   // A
              (btn_r  << 6) +   // R
              (btn_zr << 7);    // ZR

  but2_send = (btn_select << 0); // Minus (-)

  lx_send = 128; ly_send = 128; cx_send = 128; cy_send = 128;

  report30[2] = but1_send;
  report30[3] = but2_send;
  report30[4] = but3_send;
  report30[5] = (lx_send << 4) & 0xF0;
  report30[6] = (lx_send & 0xF0) >> 4;
  report30[7] = ly_send;
  report30[8] = (cx_send << 4) & 0xF0;
  report30[9] = (cx_send & 0xF0) >> 4;
  report30[10] = cy_send;

  xSemaphoreGive(xSemaphore);
  timer += 1;
  if (timer == 255) timer = 0;

  // --- CORREGIDO: USAR LA SINTAXIS CLÁSICA COINCIDENTE CON EL LINKER ---
  if (connected) {
    esp_bt_hid_device_send_report(ESP_HIDD_REPORT_TYPE_INTRDATA, 0x30, sizeof(report30), report30);
    vTaskDelay(15 / portTICK_RATE_MS);
  } else {
    esp_bt_hid_device_send_report(ESP_HIDD_REPORT_TYPE_INTRDATA, 0x30, sizeof(dummy), dummy);
    vTaskDelay(100 / portTICK_RATE_MS);
  }
}

void send_task(void* pvParameters) {
  while (1) {
    send_buttons();
  }
}

void startBlink() {
  while (1) {
    gpio_set_level(LED_GPIO, 0); vTaskDelay(150 / portTICK_RATE_MS);
    gpio_set_level(LED_GPIO, 1); vTaskDelay(150 / portTICK_RATE_MS);
    gpio_set_level(LED_GPIO, 0); vTaskDelay(150 / portTICK_RATE_MS);
    gpio_set_level(LED_GPIO, 1); vTaskDelay(1000 / portTICK_RATE_MS);
  }
}

// Estructura de Callback nativa original del firmware
void connection_cb(esp_bd_addr_t bd_addr, esp_hidd_connection_state_t state) {
  if (state == ESP_HIDD_CONN_STATE_CONNECTED) {
    esp_bt_gap_set_scan_mode(ESP_BT_NON_CONNECTABLE, ESP_BT_NON_DISCOVERABLE);
    if (BlinkHandle != NULL) { vTaskDelete(BlinkHandle); BlinkHandle = NULL; }
    gpio_set_level(LED_GPIO, 1);
    connected = true;
    if (SendingHandle != NULL) { vTaskDelete(SendingHandle); SendingHandle = NULL; }
    xTaskCreatePinnedToCore(send_task, "send_task", 2048, NULL, 2, &SendingHandle, 0);
  } else if (state == ESP_HIDD_CONN_STATE_DISCONNECTED) {
    xTaskCreate(startBlink, "blink_task", 1024, NULL, 1, &BlinkHandle);
    paired = 0; connected = false;
    esp_bt_gap_set_scan_mode(ESP_BT_CONNECTABLE, ESP_BT_GENERAL_DISCOVERABLE);
  }
}

